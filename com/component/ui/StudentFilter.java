    private void formWindowOpened(java.awt.event.WindowEvent evt) {
        DateTimeFormatter dtf= DateTimeFormatter.ofPattern("yyyy-MM-dd");
        LocalDate now = LocalDate.of(2018, 12, 24);
        if((now.getMonthValue()==12)|| (now.getMonthValue()==1 && now.getDayOfMonth()<3)){
            jButton1.setEnabled(true);
        }else if((now.getMonthValue()==5)|| (now.getMonthValue()==6 && now.getDayOfMonth()<3)){
            jButton1.setEnabled(true);
        }else{
            jButton1.setEnabled(false);
        }
    }

