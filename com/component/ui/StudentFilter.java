    void loadStudent(){
        int x=0;
        String y=null;
        
        if(cbt.getSelectedItem().toString().equals("All")){
            x=0;
        }
        else if(!cbt.getSelectedItem().toString().equals("All")){
            x=1;
            y=cbt.getSelectedItem().toString();
        }
        sList = Student.Helper.getStudent(x, y);
        StudentListView SD = new StudentListView(sList);
        stable.setModel(SD);
        stable.setShowGrid(true);
        jScrollPane1.getViewport().setBackground(new java.awt.Color(204, 204, 204));
        jScrollPane1.setBorder(null);
        stable.getColumnModel().getColumn(0).setPreferredWidth(10);
        stable.getColumnModel().getColumn(1).setPreferredWidth(70);
        stable.getColumnModel().getColumn(2).setPreferredWidth(70);
        stable.getColumnModel().getColumn(3).setPreferredWidth(100);
        stable.setRowHeight(20);
        txtn.setText(Integer.toString(stable.getRowCount()));
    }

